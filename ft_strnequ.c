#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int		i;

	i = 0;
	if (n == 0)
		return (1);
	while (n > 0 && s1[i] == s2[i])
	{
		n--;
		if (n == 0 && s1[i] == s2[i])
			return (1);
		i++;
	}
	if (s1[i] != s2[i])
		return (0);
	return (1);
}
