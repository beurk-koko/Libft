#include "libft.h"

#include "libft.h"

void	ft_putnbr_fd(int n)
{
	if (n < 0)
	{
		ft_putchar_fd('-');
		n = n * -1;
	}

	if (n >= 10)
	{
		ft_putnbr_fd(n / 10);
		ft_putnbr_fd(n % 10);
	}
	if (n < 10 && n > 0)
		ft_putchar_fd(n + '0');
}
