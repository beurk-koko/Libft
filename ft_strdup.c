#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	int		i;

	i = 0;
	s2 = (char*)malloc(sizeof(*s2) * i);
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i + 1] = '\0';
	return(s2);
}