#include <libft.h>

void	bzero(void *s, size_t n)
{
	size_t	index;
	char	*str;

	index = 0;
	if (n != 0)
	{
		str = s;
		while (index < n)
		{
			str[index] = 0;
			index++;
		}
	}
}
